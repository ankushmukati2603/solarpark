            <h2 class="text-center mb-40">New National Biogas and Organic Manure Programme (NNBOMP)</h2>
            <h4>Introduction</h4>
            <p>There is ample potential of setting up biogas plants in India considering the livestock population of
                512.06 million, which includes about 300 million (299.98 million) total population of bovines
                (comprising of cattle, buffalo, mithun and yak). The livestock sector contributes about significantly to
                India’s GDP and will continue to increase. The dissemination of biogas technology is a boon for Indian
                farmers with its direct and collateral benefits.</p>
            <p>The Ministry of New and Renewable Energy promotes installation of biogas plants by implementing Central
                Sector Schemes under Off-Grid/distributed and decentralized Renewable Power. The two on going schemes
                are:</p>
            <ul>
                <li>New National Biogas and Organic Manure Programme (NNBOMP), for Biogas Plant size ranging from 1
                    cu.m. to 25 cu.m. per day.</li>
                <li>Biogas Power Generation (Off-grid) and Thermal energy application Programme (BPGTP), for setting up
                    biogas plants in the size range of 30 m<sup>3</sup> to 2500 m<sup>3</sup> per day, for corresponding
                    power generation capacity range of 3 kW to 250 kW from biogas or raw biogas for thermal energy /
                    cooling applications.</li>
            </ul>
            <p>Biogas contains about 55-65 % of methane, 35- 44 % of carbon dioxide and traces of other gases, such as
                Hydrogen Sulphide, Nitrogen and Ammonia. Biogas, in its raw form, that is without any purification can
                be used as clean cooking fuel like LPG, lighting, motive power and generation of electricity. It can be
                used in diesel engines to substitute diesel up to 80% and up to 100% replacement of diesel by using 100%
                Biogas Engines. Further, Biogas can be purified and upgraded up to 98% purity of methane content to make
                it suitable to be used as a green and clean fuel for transportation or filling in cylinders at high
                pressure of 250 bar or so and called as Compressed Bio-Gas (CBG).</p>
            <p>Initially, Biogas Plants were developed for digesting cattle dung. However, over a period of time,
                technology has been developed for the bio-methanation of various types of biomass materials and organic
                wastes. Biogas plant designs are now available from 0.5 M3 to 1000 M3 unit size or more and multiples of
                that can be installed for achieving higher Biogas Plant sizes, depending upon availability of the raw
                material such as for family/ household, small farmers, dairy farmers and for community, institutional
                and industrial/ commercial applications. The unit size of industrial and municipal wastes based biogas
                plants may go up to 15000 M3 to 20000 M3 biogas production per day.</p>
            <h4 class="mt-25">About the programme</h4>
            <p>The Ministry of New and Renewable Energy, Government of India, launched the programme “New National
                Biogas and Organic Manure Programme (NNBOMP)” with the following objectives:</p>
            <ul>
                <li>To provide clean cooking fuel for kitchens, lighting and meeting other thermal and small power needs
                    of farmers/dairy farmers /users including individual households and to improve organic manure system
                    based on bio slurry from biogas plants in rural and semi urban areas by setting up of biogas plants.
                </li>
                <li>To mitigate drudgery of women and time saving for them for other livelihood activities and reduce
                    pressure on forests and accentuate social benefits.</li>
                <li>To improve sanitation in rural and semi-urban areas including linking sanitary toilets with cattle
                    dung biogas plants.</li>
                <li>To provide biogas plant produced slurry (liquid / semi-solid or dried) as an organic enriched
                    bio-manure to help reduce use of chemical fertilizers such as urea, linking biogas slurry with
                    enrichment units such as vermicomposting, Phosphate Rich Organic Manure (PROM) plants and other
                    organic enrichment facilities as a source of value addition to bio gas plant slurry.</li>
                <li>To meet ‘lifeline energy’ needs for cooking as envisaged in “Integrated Energy Policy” of NITI Aayog
                    (erstwhile Planning Commission).</li>
                <li>To help in combating and reduction in causes of climate change by preventing emissions of Green
                    House Gases ( GHGs) such as carbon dioxide and methane into the atmosphere.</li>
            </ul>
            <h4 class="mt-25">Eligibility Criteria</h4>
            <p>Eligibility for small biogas plant installation under NNBOMP.</p>
            <ul>
                <li>Beneficiary should have their own land/ space about 50-60 Square meter area for installing small
                    biogas plant.</li>
                <li>Availability of cow dung/ feedstock’s & regular water supply.</li>
                <li>Financial capacity for investing their own share money for biogas.</li>
            </ul>
